#!/usr/bin/env node
// Extracts the data-friends / data-prey / data-foes CSV attributes
// from the GDD's cell + pathogen cards (per CLAUDE.md, docs/ is the
// canonical source of truth) and emits a runtime-importable JS
// module that the in-game start banner reads.
//
// Run from repo root:
//   node tools/extract-cell-relations.mjs
//
// Re-run whenever a card's relations change in docs/ (the
// generated assets/core/cell-relations.js is a build artefact â€”
// committed for the no-build-step constraint, but should always
// reflect what's in docs/).

import { readFileSync, writeFileSync } from 'node:fs';

const SOURCES = [
  { path: 'docs/ch01-helden.md',     nameClass: 'cell-name'     },
  { path: 'docs/ch02-pathogene.md',  nameClass: 'pathogen-name' },
];

// Mapping from the German display names used in the cards to the
// canonical cell-type keys (the same keys CELL_TYPES uses in
// assets/core/state.js). Heroes + pathogens that share a key with
// the doc display are passed through as-is.
const NAME_TO_KEY = {
  // Heroes (ch01)
  'Makrophage': 'macrophage',
  'Neutrophile': 'neutrophil',
  'Dendritische Zelle': 'dendritic',
  'T-Helferzelle': 'tcell',     // doc has Helfer + Killer; game has one tcell.
  'T-Killerzelle': 'tcell',     // We keep separate doc cards but map both
                                 // to 'tcell' so the runtime lookup works.
  'B-Zelle': 'bcell',
  'Natural Killer': 'nk',
  'Mast Cell': 'mast',
  'Eosinophil': 'eosinophil',
  'Basophil': 'basophil',
  'Monocyte': 'monocyte',
  'Platelet': 'platelet',
  // Pathogens (ch02)
  'Standard-Virus': 'virus',
  'Corona-Virus': 'virus',       // game's `virus` covers all subtypes
  'Influenza': 'virus',
  'Bakteriophage': 'virus',
  'Retrovirus': 'virus',
  'Germ': 'germ',
  'Bacterium': 'bacterium',
  'AmĂ¶be': 'amoebaP',
  'Mite': 'mite',
  'Slime': 'slime',
  'Spore': 'spore',
  'Toxin': 'toxin',
};

const out = {};

function parseCsv(s) {
  return s.split(',').map(t => t.trim()).filter(Boolean);
}

for (const src of SOURCES) {
  const html = readFileSync(src.path, 'utf8');
  // Match each card by anchoring on its name div, then look ahead
  // for the next data-friends/data-prey/data-foes attrs.
  const nameRe = new RegExp(
    `<div class="${src.nameClass}">\\s*([^<]+?)\\s*</div>` +
    `[\\s\\S]*?` +
    `data-friends="([^"]*)"\\s+data-prey="([^"]*)"\\s+data-foes="([^"]*)"`,
    'g',
  );
  let m;
  while ((m = nameRe.exec(html)) !== null) {
    const [, name, friends, prey, foes] = m;
    const key = NAME_TO_KEY[name];
    if (!key) {
      console.warn(`  âś— no key mapping for "${name}" in ${src.path} â€” skipped`);
      continue;
    }
    // If multiple doc cards collapse to the same runtime key
    // (e.g. all virus subtypes â†’ 'virus'), KEEP the first one we
    // see â€” that's the canonical entry. Subtypes' relations are
    // the same anyway.
    if (out[key]) continue;
    out[key] = {
      friends: parseCsv(friends),
      prey:    parseCsv(prey),
      foes:    parseCsv(foes),
    };
    console.log(`  âś“ ${key}  (from "${name}")`);
  }
}

const json = JSON.stringify(out, null, 2);
const banner = `// Auto-generated by tools/extract-cell-relations.mjs from
// docs/ch01-helden.md + docs/ch02-pathogene.md. DO NOT EDIT BY HAND
// â€” change the source cards in docs/ and re-run the extractor.
// Per CLAUDE.md, docs/ is the master source of truth for these
// relationships; this file mirrors them so the in-game start
// banner doesn't need to fetch + parse HTML at runtime.
//
// Each value is { friends: cellKey[], prey: cellKey[], foes: cellKey[] }.
`;

writeFileSync(
  'assets/core/cell-relations.js',
  banner + 'export const CELL_RELATIONS = ' + json + ';\n',
);
console.log(`\nWrote ${Object.keys(out).length} entries to assets/core/cell-relations.js`);
